package Course13;

public class Practice04 {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {

			if (i == 3) {
				System.out.println("3週目だよ");
			}
			if (i == 6) {
				System.out.println("あと4週だよ");
			}
			if (i == 10) {
				System.out.println("最後のループ");
			}
			System.out.println(i);
		}
	}
}
